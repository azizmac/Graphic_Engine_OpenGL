#include "Cube.h"

#include <glm/ext.hpp>

bbe::Cube::Cube() 
    : Tranformable(),
    _VBO({
    -0.5f, -0.5f, -0.5f,
     0.5f, -0.5f, -0.5f,
     0.5f,  0.5f, -0.5f,
     0.5f,  0.5f, -0.5f,
    -0.5f,  0.5f, -0.5f,
    -0.5f, -0.5f, -0.5f,

    -0.5f, -0.5f,  0.5f,
     0.5f, -0.5f,  0.5f,
     0.5f,  0.5f,  0.5f,
     0.5f,  0.5f,  0.5f,
    -0.5f,  0.5f,  0.5f,
    -0.5f, -0.5f,  0.5f,

    -0.5f,  0.5f,  0.5f,
    -0.5f,  0.5f, -0.5f,
    -0.5f, -0.5f, -0.5f,
    -0.5f, -0.5f, -0.5f,
    -0.5f, -0.5f,  0.5f,
    -0.5f,  0.5f,  0.5f,

     0.5f,  0.5f,  0.5f,
     0.5f,  0.5f, -0.5f,
     0.5f, -0.5f, -0.5f,
     0.5f, -0.5f, -0.5f,
     0.5f, -0.5f,  0.5f,
     0.5f,  0.5f,  0.5f,

    -0.5f, -0.5f, -0.5f,
     0.5f, -0.5f, -0.5f,
     0.5f, -0.5f,  0.5f,
     0.5f, -0.5f,  0.5f,
    -0.5f, -0.5f,  0.5f,
    -0.5f, -0.5f, -0.5f,

    -0.5f,  0.5f, -0.5f,
     0.5f,  0.5f, -0.5f,
     0.5f,  0.5f,  0.5f,
     0.5f,  0.5f,  0.5f,
    -0.5f,  0.5f,  0.5f,
    -0.5f,  0.5f, -0.5f
    })
{

    _shader = new Shader("shaders/vertexShader.vert", "shaders/fragmentShader.frag");
    
    

    _VAO.bind();
    
    _VAO.setAttrib(_VBO, 0, 3, GL_FLOAT, 3 * sizeof(float), (void*)0);
    _VAO.unbind();
    _VBO.unbind();


}

bbe::Cube::~Cube()
{
    delete _shader;
}

bbe::Shader* bbe::Cube::getShader()
{
    return _shader;
}

void bbe::Cube::draw()
{
    _shader->use();
    _shader->setMat4("model", getTransformMatrix());
    _VAO.bind();
    glDrawArrays(GL_TRIANGLES, 0, sizeof(vertices) / 3);
}
